<h1 class="mb-4 text-4xl font-extrabold leading-none tracking-tight text-gray-900 md:text-5xl lg:text-6xl">How To Connect</h1>
<ul class="list-disc list-inside">
	<li>
		Need a OpenSimulator compatible viewer like <a href="https://www.firestormviewer.org/os-operating-system/" class="hover:underline font-medium">Firestorm</a> or <a href="https://www.singularityviewer.org/downloads/" class="hover:underline font-medium">Singularity</a><br>
		<small>Ven suggest Singularity as it appears more stable and less bug splats</small>
	</li>
	<li>Create a avatar for this grid at <a href="{{ url('join') }}">{{ url('join') }}</a></li>
	<li>Once a viewer is installed open it, before logging in you need to add our grid to your viewer's grid list</li>
	<li>Press Ctrl+P to open preferrences, go to Opensim tab on the left, in the text box for Address put in <span class="text-red-800">canadiangrid.ca:8002</span> and hit apply, give it afew seconds and click Ok at the bottom.</li>
	<li>For Singularity there is a Grid Manager button on the login screen, click that, add our address in Login URI: text box, hit Refresh button below it then OK</li>
	<li>On the login screen at the bottom there is a grid selector dropdown box, open that and Select Canadian Grid.</li>
	<li>Login with your Firstname and Lastname (Resident if no last name)</li>
	<li>Welcome to the Canadian Grid</li>
</ul>